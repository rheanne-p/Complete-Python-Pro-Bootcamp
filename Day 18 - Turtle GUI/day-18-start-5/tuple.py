# Python Tuples
my_tuple = (1, 3, 8)

# Use index to get item in tuple
my_tuple[1]

# You can't change values of a tuple
# constant and immutable
my_tuple[2] = 12
# TypeError: 'tuple' object does not support item assignment

# Changing values of a tuple
list(my_tuple)
